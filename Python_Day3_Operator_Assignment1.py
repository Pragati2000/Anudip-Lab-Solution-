# Using input() function take one number from the user and using ternary operators check whether the number is even or odd


# Take input from the user
num = int(input("Enter a Number:"))
# Check if the number is even using a ternary operator
if num % 2 == 0:
# Print the result
  print("Given number is Even")
# Check else the number is odd using a ternary operator 
else:
# Print the result
  print("Given number is Odd")


"""Output
  Enter a Number:1
  Given number is Odd"""

 
